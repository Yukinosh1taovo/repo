// var str = new String("String");
var str = " String    ";

console.log("str: " + str);

// length 长度属性
console.log("str.length: " + str.length);

// charAt() 输出索引字符
console.log("charAt(3): " + str.charAt(3));

// indexOf() 检索字符串位置
console.log("indexOf(tr): "+ str.indexOf("tr"));

// trim()  去除字符串左右两边空格
console.log("str.trim(): " + str.trim());

// substring(start, end)  含头不含尾
console.log("str.substring(1, 4): " + str.substring(1, 4));


